// 获取随机牌（可指定数量）
function drawTarotCards(count = 1) {
    const shuffled = [...TAROT_CARDS].sort(() => Math.random() - 0.5);
    return shuffled.slice(0, count).map(card => ({
        ...card,
        isReversed: Math.random() < 0.3 // 30% 概率逆位，不覆盖 reversed 含义文本
    }));
}
